<h style="color:blue;"><b>Σπλάντζια</b></h>
<p>Η Σπλάντζια κατά την περίοδο της Οθωμανικής κυριαρχίας ήταν η κατ' εξοχήν τουρκική συνοικία. Εκτείνεται νότια και ανατολικά από τον περιτειχισμένο λόφο του Καστελίου στην παλιά πόλη των Χανίων. Εκεί δημιουργήθηκε ο Τουρκικός μαχαλάς της πόλης με κεντρική πλατεία αυτή που τώρα έχει μετονομαστεί σε πλατεία 1821.
Την εποχή εκείνη, η εκκλησία του Αγίου Νικολάου στην πλατεία της Σπλάντζιας που αποτελούσε τμήμα της μονής των Δομηνικανών Μοναχών, μετατρέπεται σε κεντρικό τζαμί της πόλης προς τιμήν του Σουλτάνου Ιμπραήμ. Είναι ο μόνος ναός σήμερα στην Ελλάδα που διατηρεί και καμπαναριό και μιναρέ. Στη βορειοδυτική πλευρά της Σπλάντζιας βρίσκεται ο ενετικός ναός του Αγίου Ρόκκου, μία στρογγυλής κάτοψης ενετική εκκλησία που έμεινε ανεπηρέαστη από την τουρκοκρατία και τις μεταγενέστερες εποχές.
Στο κέντρο της Σπλάντζιας στέκει ένας τεράστιος πλάτανος. Από κάτω υπήρχε ένα οκτάγωνο αραβικό κιόσκι, όπου είχαν δικαίωμα να κάθονται τιτλούχοι της πολιτείας, επιφανείς μπέηδες και αγάδες. Στον πλάτανο της Σπλάντζιας επί Τουρκοκρατίας, Έλληνες χριστιανοί απαγχονίστηκαν, εκτελέστηκαν και βασανίστηκαν. Τις πρώτες μέρες της Επανάστασης του 1821, οι Τούρκοι ανησυχώντας για μεταφορά της στην Κρήτη, απαγχόνισαν τον επίσκοπο Κισσάμου Μελχισεδέκ Δεσποτάκη (19 Μαΐου 1821). Το ίδιο συνέβη και στον ιεροδιάκονο από τη Βέροια Καλλίνικο. Στην πλατεία υπάρχει μια πλάκα στη μνήμη τους.
Ανάμεσα στην εκκλησία του Αγίου Νικολάου και τον πλάτανο στη Σπλάντζια υπάρχει η τουρκική υπόγεια κρήνη περιτριγυρισμένη από κάγκελα. Η δεξαμενή είχε τέτοια χωρητικότητα, ώστε να καλύπτει την πόλη των Χανίων για έξι μήνες.
Τα τελευταία χρόνια η συνοικία της Σπλάντζιας έχει αναμορφωθεί πλήρως και από φτωχή περιοχή με σκοτεινά σοκάκια έχει μετατραπεί σε μια από τις πιο δημοφιλείς συνοικίες της πόλης. ’νοιξαν δεκάδες καφέ, μεζεδοπωλεία και εστιατόρια. Η περιοχή κατάφερε να παντρέψει όλους τους πολιτισμούς που πέρασαν από τα Χανιά.
