<h style="color:blue;"><b> Δημοτική Αγορά Χανίων</b></h><p>Η Δημοτική Αγορά Χανίων, το τεράστιο κτίσμα των 4.000 τ.μ. επί της Χατζημιχάλη Γιάνναρη, είναι ένα από τα σημαντικότερα νεότερα μνημεία της πόλης των Χανίων. Κτίστηκε το 1913 πάνω στον προμαχώνα Piatta Forma, στη μέση των τειχών της Ενετικής περιόδου.  Στην ουσία επρόκειτο για μια σοβαρή παρέμβαση εκσυγχρονισμού και ενοποίησης της παλιάς με την νέα, αναπτυσσόταν ραγδαία έξω από τα βενετσιάνικα τείχη.
Μέχρι τα τέλη του 19ου αιώνα στη Δημοτική Αγορά Χανίων βρίσκονταν πρόχειρα εγκατεστημένα κρεοπωλεία, ιχθυοπωλεία, και καταστήματα λαχανικών, μια  υπαίθρια λαϊκή αγορά δηλαδή, όπου οι χωρικοί έφερναν τα προϊόντα τους να τα πουλήσουν.
Το 1908 ο Δήμος αποφάσισε να καλλωπίσει το μέρος χτίζοντας την Δημοτική αγορά Χανίων, που για να ανεγερθεί χρειάστηκε δάνειο 300.000 δραχμών από την Τράπεζα Κρήτης. Το σχέδιο ήταν του ντόπιου μηχανικού Κ. Δρανδάκη, που ακολούθησε το πρότυπο  της σκεπαστής αγοράς της Μασσαλίας. Έχει σχήμα σταυρού και τέσσερεις πόρτες, μία σε κάθε πτέρυγα. Στεγάζει 76 καταστήματα, διατεταγμένα κατά ομάδες στις τέσσερις κεραίες του σταυρού.
