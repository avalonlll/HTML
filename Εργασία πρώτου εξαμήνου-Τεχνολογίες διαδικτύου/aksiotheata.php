<h style="color:blue;"><b>Μητρόπολη των Χανίων</b></h>
<p>Ο καθεδρικός ναός των Εισοδίων της Θεοτόκου (Τριμάρτυρη), η Μητρόπολη των Χανιών, βρίσκεται στο μέσον της οδού Χάληδων στην παλιά πόλη.
Χτίστηκε στη θέση παλαιότερου ναού του 14ου αιώνα. Επί Τουρκοκρατίας η Μητρόπολη των Χανίων μετατράπηκε σε σαπωνοποιείο, και εκεί που είναι σήμερα το καμπαναριό ήταν το καζάνι πού έβραζαν τα υλικά για το σαπούνι. Ωστόσο, σε μια αποθήκη μέσα στο ναό εξακολουθούσε να φυλάσσεται η εικόνα των Εισοδίων της Παναγίας και μπροστά της άναβε πάντα ένα καντήλι, με την ανοχή του Τούρκου Πασά, διοικητή των Χανίων.
Ο θρύλος της Μητρόπολης των Χανίων λέει πως στα μέσα του 19ου αιώνα, στο σαπωνοποιείο δούλευε ο Τσερκάκης, στον οποίο εμφανίστηκε η Παναγία σε όραμα και του ζήτησε να φύγει, γιατί δεν ήθελε το σπίτι της να είναι σαπουναριό. Ο τεχνίτης έφυγε παίρνοντας μαζί του και την εικόνα. Λίγο αργότερα, το παιδί του Πασά έπεσε στο πηγάδι,  νότια του ναού. Στην απόγνωσή του ο Τούρκος επικαλέστηκε τη βοήθεια της Παναγίας, με αντάλλαγμα να επιστρέψει το ναό στους χριστιανούς. Το παιδί σώθηκε και ο Πασάς πιστός στο λόγο του ξεκίνησε την οικοδόμηση του καινούριου ναού.
Η κατασκευή της Μητρόπολης των Χανίων, του Καθεδρικού ναού των Εισοδίων της Θεοτόκου ολοκληρώθηκε το 1860 στη μορφή της τρίκλιτης βασιλικής με υπερυψωμένο το μεσαίο κλίτος. Τα τρία κλίτη της έδωσαν και το όνομα Τριμάρτυρη. Κοσμείται με μεγάλες και εντυπωσιακές αγιογραφίες έργα των Γ. Καλλιτεράκη, Γ. Σταυράκη, Ε. Τριπολιτάκη και Δ. Κοκότση.
Η Μητρόπολη των Χανίων γιορτάζει στις 21 Νοεμβρίου, στα Εισόδια της Θεοτόκου, πολιούχο των Χανιών.
