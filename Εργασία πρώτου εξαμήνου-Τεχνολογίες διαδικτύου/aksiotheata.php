<h style="color:blue;"><b>Ενετικό Λιμάνι</b></h>
<p>Ενετικό Λιμάνι	 
Το Ενετικό λιμάνι των Χανίων με τον Φάρο αποτελεί σήμα κατατεθέν της πόλης των Χανίων. Σήμερα συγκεντρώνει πλήθος επισκεπτών και Χανιωτών, κυρίως τους θερινούς μήνες. Πολλά εστιατόρια, ταβέρνες, καφετέριες, μπαρ κατά μήκος της παραλίας προσφέρονται για χαλαρές ώρες αναψυχής και ψυχαγωγίας.
Κατασκευάστηκε από τους Ενετούς από το 1320 μέχρι το 1356. Το Ενετικό λιμάνι εξυπηρετούσε το εμπόριο αλλά και τον έλεγχο της θάλασσας της Κρήτης για την καταπολέμηση της πειρατείας. Τον 16ο αιώνα, λόγω της διαφαινόμενης τουρκικής απειλής, τα νέα βενετσιάνικα σχέδια της οχύρωσης της πόλης συμπεριελάμβαναν και το λιμάνι. Κατασκευάζεται το φρούριο Φιρκά στην είσοδο του λιμανιού στα δυτικά και ενισχύεται  με τον Προμαχώνα του Αγίου Νικολάου του Μώλου πάνω στον λιμενοβραχίονα. Σε περίπτωση απειλής μία χοντρή αλυσίδα έδενε από το Φρούριο Φιρκά στην απέναντι βάση του Φάρου κλείνοντας της είσοδο του λιμανιού.
Την περίοδο της Τουρκοκρατίας το Ενετικό λιμάνι παρότι αποτελούσε κέντρο εμπορίου αφήνεται στην τύχη του. Μόνο στα χρόνια της Αιγυπτιοκρατίας (1831-1841) ξεκινούν πάλι επισκευαστικές εργασίες και ανακαινίζεται και ο Φάρος.
Από τα τέλη του 19ου αιώνα το Ενετικό λιμάνι αδυνατεί να εξυπηρετήσει τα μεγαλύτερα  πλοία της εποχής και σταδιακά εγκαταλείπεται και χρησιμοποιείται το μεγάλο φυσικό λιμάνι της Σούδας το οποίο μέχρι σήμερα εξυπηρετεί την εμπορική και επιβατική κίνηση της δυτικής Κρήτης.
Αξίζει να δείτε  στο Ενετικό λιμάνι από τα δυτικά στα ανατολικά:
Το φρούριο Φιρκά με το Ναυτικό Μουσείο
Το Σαντριβάνι (πλατεία Ελευθερίου Βενιζέλου)
Το Γιαλί Τζαμί (το παλαιότερο μουσουλμανικό οικοδόμημα της Κρήτης)
Τα βυζαντινά τείχη του λόφου του Καστελίου
Το Μεγάλο Αρσενάλι
Τα Ενετικά νεώρια
Τον Προμαχώνα Αγίου Νικολάου του Μώλου πάνω στον λιμενοβραχίονα
Τον Φάρο
