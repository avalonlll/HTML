<h><b>Ιστορία</b><h>
<p>Στην περιοχή των Χανίων καταγράφεται ιστορία 50 και πλέον αιώνων, με τον επισκέπτη να περπατά
 σε χώρους και να βλέπει τα σημάδια που άφησαν πολιτισμοί εδώ και περισσότερα από 5000 χρόνια όπου κατοικείται,
 αφήνοντας ισχυρό στίγμα στο νησί της Κρήτης αλλά και στην ευρύτερη περιοχή της Μεσογείου. 
 Ιστορία, φυσικό τοπίο, μνημεία και ευρήματα δένουν αρμονικά στη σύγχρονη πόλη των Χανίων,
 σε ένα μοναδικό ταξίδι αισθήσεων από την προϊστορία μέχρι το παρόν.
Η περιοχή των Χανίων κατοικείται από την πρωτομινωική περίοδο (3650 π.Χ.). 
Σύμφωνα με την παράδοση, η αρχαία Κυδωνία ιδρύθηκε κατά τα προϊστορικά χρόνια από τον Κύδωνα, 
γιο του Ερμή -ή κατά άλλους γιο του Απόλλωνα- και της νύμφης Ακακαλλίδας, κόρης του Μίνωα.
 Ο λόφος Καστέλι, πάνω από το παλιό λιμάνι, αποτέλεσε το σημείο του πρώτου οικισμού,
 ο οποίος εξελίχθηκε και αναπτύχθηκε συνδυάζοντας τη γεωργία με το εμπόριο και τη ναυτιλία.
 Στην Οδύσσεια του Ομήρου οι Κύδωνες αναφέρονται ως μία από τις πέντε κρητικές φυλές,
 ενώ η Κυδωνία θεωρείται από το Στράβωνα ως την τρίτη μεγαλύτερη πόλη στο νησί.</p>
 
 
 
